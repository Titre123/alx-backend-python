#!/usr/bin/env python3
'''
    1-concat:
    Write a type-annotated function concat that takes a string
    str1 and a string str2 as arguments and returns a concatenated string
'''


def concat(str1: str, str2: str) -> str:
    '''
    Args:
        @str1 - str
        @str2 - str
    Returns:
        concat str1 and str2 -> str
    '''
    return str1 + str2
